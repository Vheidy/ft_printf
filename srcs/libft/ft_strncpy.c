#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char *d;

	d = dst;
	while (len--)
	{
		if (*src == '\0')
			*d++ = '\0';
		else
			*d++ = *src++;
	}
	return (dst);
}
