#include "libft.h"

size_t	ft_word(const char *s, char c)
{
	size_t	w;

	w = 0;
	while (*s)
	{
		if (*s != c && (*(s + 1) == c || *(s + 1) == '\0'))
			w++;
		s++;
	}
	return (w);
}
